import java.util.*;

public class Pair<K extends Comparable<K>,V> implements Comparable<Pair<K,V>>
{
	//===============================================[Variables]====================================================

	private K key;
	private V value;
	
	//==============================================[Constructors]==================================================
	
	public Pair(K newKey, V newValue)
	{
		key = newKey;
		value = newValue;
	}
	
	//--------------------------------------------------------------------------------------------------------------
	
	
	
	//===============================================[Accessors]====================================================

	public K getKey()
	{
		return key;
	}

	//--------------------------------------------------------------------------------------------------------------
	
	public V getValue()
	{
		return value;
	}

	
	//===============================================[Functions]====================================================

	@Override
	public int compareTo(Pair<K, V> otherPair) {
		
		return getKey().compareTo(otherPair.getKey());		
	}

	//--------------------------------------------------------------------------------------------------------------
	//--------------------------------------------------------------------------------------------------------------
	//--------------------------------------------------------------------------------------------------------------
	//--------------------------------------------------------------------------------------------------------------
	//--------------------------------------------------------------------------------------------------------------
	//--------------------------------------------------------------------------------------------------------------
	//--------------------------------------------------------------------------------------------------------------
	//--------------------------------------------------------------------------------------------------------------
	//--------------------------------------------------------------------------------------------------------------
}	// End of class